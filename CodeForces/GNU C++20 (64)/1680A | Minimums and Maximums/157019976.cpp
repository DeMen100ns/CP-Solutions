const int N = 2e5 + 5;
const long long INF = 1e18 + 7;
const int MAXA = 1e9;
const int B = sqrt(N) + 5;
 
void solve()
{
    int l1, r1, l2, r2;
    cin >> l1 >> r1 >> l2 >> r2;
    int ans = l1 + l2;
    if (l1 <= r2 && l1 >= l2) ans = min(ans, l1);
    if (l2 <= r1 && l2 >= l1) ans = min(ans, l2);
    cout << ans << endl;
}
 
