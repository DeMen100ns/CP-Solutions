const int N = 2e5 + 5;
const long long INF = 1e18 + 7;
const int MAXA = 4e5;
const int B = sqrt(N) + 5;
 
vector <int> u[2 * N];
 
int lcm(int a, int b){
    return a / __gcd(a, b) * b;
}
 
void precal(){
    for(int i = 1; i <= MAXA; ++i){
        for(int j = 1; j * j <= i; ++j){
            if (i % j == 0){
                u[i].push_back(j);
                if (j * j < i) u[i].push_back(i / j);
            }
        }
    }
}
 
void solve()
{
    int l, r; long long ans; cin >> l >> r;
    ans = (r - l + 1);
    ans = ans * (ans - 1) * (ans - 2) / 6;
    for(int k = l; k <= r; ++k){
        for(int i = 0; i < (int)u[2 * k].size(); ++i){
            int I = u[2 * k][i];
            if (I < l || I >= k) continue;
            int tmp = lcm(I, k);
            for(int j = i + 1; j < (int)u[2 * k].size(); ++j){
                int J = u[2 * k][j];
                if (J < l || J >= k) continue;
                
                if (lcm(tmp, J) < I + J + k) --ans;
            }
        }
    }
    cout << ans << endl;
}
 
