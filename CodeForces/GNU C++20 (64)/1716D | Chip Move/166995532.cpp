const int N = 2e5 + 5;
const long long INF = numeric_limits<long long>::max() / 2;
const int MAXA = 1e9;
const int B = sqrt(N) + 5;
 
int dpold[N], dpnew[N], ans[N];
 
void solve()
{
    int n, k; cin >> n >> k;
    dpold[0] = 1;
    for(int s = k; s <= n; s += k, k++){
        for(int i = k; i <= n; ++i){
            dpnew[i] = sum(dpnew[i - k], dpold[i - k]);
            add(ans[i], dpnew[i]);
        }
        for(int i = 0; i <= n; ++i){
            dpold[i] = dpnew[i];
            dpnew[i] = 0;
        }
    }
    for(int i = 1; i <= n; ++i) cout << ans[i] << " ";
}
 
